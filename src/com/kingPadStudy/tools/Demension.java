package com.kingPadStudy.tools;

public class Demension {
	int height;
	int width;
	
	public Demension(int i, int j) {
		setWidth(i);
		setHeight(j);
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	
	
}
